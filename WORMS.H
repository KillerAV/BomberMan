void passthroughworm(int x,int y)	//to create worms that can pass through soft blocks
{
	setcolor(DARKGRAY);
	rectangle(x-7,y-7,x+7,y+7);
	for(int i=x-8,j=y-8;i<=x+8;i+=3)
	{
		putpixel(i,j,DARKGRAY);
		putpixel(i,j,DARKGRAY);
		putpixel(i+1,j+1,DARKGRAY);
		}
	for(j=y-8,i=x-8;j<=y+8;j+=3)
	{
		putpixel(i,j,DARKGRAY);
		putpixel(i,j,DARKGRAY);
		putpixel(i+1,j+1,DARKGRAY);
		}
	for(i=x-8,j=y+8;i<=x+8;i+=3)
	{
		putpixel(i,j,DARKGRAY);
		putpixel(i,j,DARKGRAY);
		putpixel(i+1,j-1,DARKGRAY);
		}
	for(j=y-8,i=x+8;j<=y+8;j+=3)
	{
		putpixel(i,j,DARKGRAY);
		putpixel(i,j,DARKGRAY);
		putpixel(i-1,j+1,DARKGRAY);
		}

	setfillstyle(1,LIGHTBLUE);
	floodfill(x,y,DARKGRAY);
	circle(x-3,y-2,2);
	circle(x+3,y-2,2);
	setfillstyle(1,WHITE);
	floodfill(x-3,y-2,DARKGRAY);
	floodfill(x+3,y-2,DARKGRAY);
	circle(x-3,y-2,1);
	circle(x+3,y-2,1);
	setfillstyle(1,DARKGRAY);
	floodfill(x-3,y-2,DARKGRAY);
	floodfill(x+3,y-2,DARKGRAY);
	circle(x,y+4,2);
	setfillstyle(1,DARKGRAY);
	floodfill(x,y+4,DARKGRAY);
	}

